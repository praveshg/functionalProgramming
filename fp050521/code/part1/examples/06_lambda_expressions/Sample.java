import java.util.*;

public class Sample {
  public static void main(String[] args) {
    /*
    Function:
    1. name 
    2. return type
    3. parameter list
    4. body

    Lambda expressions (anonymous functions):
    1. parameter list
    2. body

    Java: (parameter list) -> body
    C#/JavaScript/...: (parameter list) => body
    Ruby: |parameter list| body
    Python: lambda parameter list: body
    Kotlin: { parameter list -> body }

    They have no name
    Return type is inferred based on the context

    In general the body is a single line body
    It is really important to keep the body as a single line
    and not write multiline lambda expression. That is a very poor
    programming practice to write multiline lambda.

    http://blog.agiledeveloper.com/2015/06/lambdas-are-glue-code.html
    */
  }
}
